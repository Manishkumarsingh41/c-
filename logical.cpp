#include<iostream>
using namespace std;
int main(){
    //We use and (&&)
    cout<<((3>1)&&(3>4))<<endl; //True -->1 & false -->0

     //We use and (OR)
    cout<<((3>1)||(3>4))<<endl; //True -->1 & false -->0

    //We use and (!)
    cout<<(!(3>1))<<endl; //True -->1 & false -->0

return 0;
}
